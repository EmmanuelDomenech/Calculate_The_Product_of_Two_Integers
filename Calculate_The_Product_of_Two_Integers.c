//This program will calculate the product of two integer number to yield a third integer number.

#include <stdio.h>
int main() 
{
  /* Variable definition: */
  int x, y, z;
  /* Variable initialization */
  x=-6;
  y=4;
  z= x * y;
  printf("Integers (x,y) and sum (z) are : %d,%d,%d \n", x,y,z);
  return 0;
}
